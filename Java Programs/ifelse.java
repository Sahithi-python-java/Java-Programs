public class ifelse {
    public static void main(String[] args) {
        int a = 10;
        int b = 15;
        if (b>a) {
            System.out.println("b is greater than a");
            
        } else {
            System.out.println("a is greater than b");
            
        }
        System.out.println("Hence it is ifelse");
    }
    
}
