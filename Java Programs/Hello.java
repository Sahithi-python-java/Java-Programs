public class Hello{
    public static void main(String[] args) {
        System.out.println("Hello World");
    }
}


//public class Hello: Defines a class named Hello. In Java, all code must reside within a class.
//public: Makes the method accessible from anywhere.
//static: Allows the method to run without creating an instance of the class.
//void: Indicates the method does not return any value.
//String[] args: An array to hold command-line arguments (can be empty if no arguments are passed).
//System.out.println("Hello World");: This line prints "Hello World" to the console.